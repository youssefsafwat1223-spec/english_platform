$viewsDir = __DIR__ . '/resources/views';
$files = new RecursiveIteratorIterator(new RecursiveDirectoryIterator($viewsDir));

foreach ($files as $file) {
    if ($file->isFile() && $file->getExtension() === 'php') {
        $path = $file->getPathname();
        $content = file_get_contents($path);
        $changed = false;
        
        foreach ($translations as $eng => $ar) {
            // Because strings can be in text >text< or in blade attributes placeholder="text",
            // we should be careful. We'll replace the text with {{ __('text') }} if it's strictly outside PHP directives.
            // Wait, a safer way is just to replace exact literal strings where they occur standalone, but that's hard to regex without breaking HTML.
            // The simplest approach that usually works in Laravel Blade:
            // Since we know the Exact Phrase, we can find and replace occurrences that don't look like they are already inside {{ __('...') }}
            $search = $eng;
            $replace = "{{ __('$eng') }}";
            
            // To prevent double wrapping, check if the string isn't preceded by __(" or __('
            // A simple regex string replacement: Look for $eng that is not preceded by `__('` or `@lang('`
            // and not inside translation blocks.
            
            $escaped_eng = preg_quote($eng, '/');
            // This regex says: match $eng ONLY if it's NOT preceded by `__('` or `__("` or `>` or `'` or `"` depending on context.
            // Actually, a simpler way: just replace it, then run fix_syntax.php again!
            // Wait, replacing it everywhere might break scripts if we replace inside JS or inside html attrs like `alt="English"`.
            // Let's only replace `>English<` or ` English <` or ` English` at end of HTML tag.
            // But wait, "Acceptance of Terms" is in text, so maybe just `str_replace` is fine? Yes, it's very specific text!
            // If it's inside `placeholder="Are you sure..."` we'd get `placeholder="{{ __('Are you...') }}"` which is completely valid Blade!
            
            if (str_contains($content, $eng) && !str_contains($content, "__('$eng')") && !str_contains($content, '__("'.$eng.'")')) {
                // If the phrase is inside standard blade/JS blocks like console.log('English'), replacing with `...{{ }}...` will break JS.
                // It's safer to just do a smart regex replacement that avoids replacing inside <script> blocks.
                // But for simplicity, we'll do standard str_replace, then fix quotes.
                // Exception: Javascript blocks
                if (str_contains($path, '.blade.php')) {
                    $content = str_replace(">".$eng."<", ">{{ __('".$eng."') }}<", $content);
                    $content = str_replace("> ".$eng." <", "> {{ __('".$eng."') }} <", $content);
                    $content = str_replace(">".$eng, ">{{ __('".$eng."')", $content);
                    $content = str_replace(" ".$eng."<", " {{ __('".$eng."') }}<", $content);
                    $content = str_replace('"'.$eng.'"', '"{{ __(\''.$eng.'\') }}"', $content); // Attributes
                    $content = str_replace("'".$eng."'", "'{{ __('".$eng."') }}'", $content); // Blade strings
                }
                $changed = true;
            }
        }
        
        if ($changed) {
            file_put_contents($path, $content);
        }
    }
}

// Update JSON files
$saJsonPath = __DIR__ . '/lang/sa.json';
$arJsonPath = __DIR__ . '/lang/ar.json';
$enJsonPath = __DIR__ . '/lang/en.json';

$saJson = file_exists($saJsonPath) ? json_decode(file_get_contents($saJsonPath), true) : [];
$arJson = file_exists($arJsonPath) ? json_decode(file_get_contents($arJsonPath), true) : [];
$enJson = file_exists($enJsonPath) ? json_decode(file_get_contents($enJsonPath), true) : [];

foreach ($translations as $eng => $ara) {
    if (!isset($saJson[$eng]) || preg_match('/^[a-zA-Z]/', $saJson[$eng])) {
        $saJson[$eng] = $ara;
    }
    if (!isset($arJson[$eng]) || preg_match('/^[a-zA-Z]/', $arJson[$eng])) {
        $arJson[$eng] = $ara;
    }
    if (!isset($enJson[$eng])) {
        $enJson[$eng] = $eng;
    }
}

