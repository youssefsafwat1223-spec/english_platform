$translations = [];

foreach ($replacements as $file => $fileReplacements) {
    if (file_exists(__DIR__ . '/' . $file)) {
        $content = file_get_contents(__DIR__ . '/' . $file);
        $originalContent = $content;
        
        foreach ($fileReplacements as $search => $replace) {
            $content = str_replace($search, $replace, $content);
            
            // Extract the Arabic string to add to sa.json
            preg_match_all("/__\('([^']+)'\)/", $replace, $matches);
            if (!empty($matches[1])) {
                foreach ($matches[1] as $match) {
                    $translations[$match] = ltrim(trim($match), '>'); // simple cleanup
                }
            }
        }
        
        if ($content !== $originalContent) {
            file_put_contents(__DIR__ . '/' . $file, $content);
        }
    }
}

// Update JSON files with new keys
$saJsonPath = __DIR__ . '/lang/sa.json';
$arJsonPath = __DIR__ . '/lang/ar.json';
$enJsonPath = __DIR__ . '/lang/en.json';

$saJson = json_decode(file_get_contents($saJsonPath), true);
$arJson = json_decode(file_get_contents($arJsonPath), true);
$enJson = json_decode(file_get_contents($enJsonPath), true);

foreach ($translations as $key => $val) {
    $saJson[$key] = $key;
    $arJson[$key] = $key;
    $enJson[$key] = $key; // Or translate to English if possible, but keeping Arabic key/value is safe fallback
}

file_put_contents($saJsonPath, json_encode($saJson, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE));
file_put_contents($arJsonPath, json_encode($arJson, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE));
file_put_contents($enJsonPath, json_encode($enJson, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE));

echo "Targeted translations applied successfully to blade views and json files.\n";
